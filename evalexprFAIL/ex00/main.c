#include "eval.h"

int		eval_expr(char *str)
{
	str = par_whitespaces(str);
	if (!str[0])
		return (0);
	return (main_operation(&str));
}

int		main(int ac, char **av)
{
	if (ac > 1)
	{
		ft_putnbr(eval_expr(av[1]));
		ft_putchar('\n');
	}
	return (0);
}
