#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char			*str;
	unsigned int	i;

	if (!(str = malloc(*src)))
		return (NULL);
	i = 0;
	while (src[i])
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
