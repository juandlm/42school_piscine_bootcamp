int	ft_find_next_prime(int nb)
{
	int n;

	n = 2;
	if (nb < 0)
		return (2);
	while (nb % n != 0)
	{
		if (n > nb)
			return (0);
		n++;
	}
	if (nb == n)
		return (1);
	return (0);
}
