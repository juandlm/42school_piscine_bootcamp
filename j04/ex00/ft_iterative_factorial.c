int	ft_iterative_factorial(int nb)
{
	int i;
	int a;

	i = 1;
	a = 1;
	if (nb < 0 || nb > 12)
	{
		return (0);
	}
	while (i <= nb)
	{
		a = a * i;
		i++;
	}
	return (a);
}
