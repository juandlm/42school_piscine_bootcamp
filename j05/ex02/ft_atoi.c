int ft_atoi(char *str)
{
	int sign;
	int res;

	sign = 1;
	res = 0;
	while (*str == '\t' || *str == '\n' || *str == '\r' 
		|| *str == '\v' || *str == '\f' || *str == ' ')
		str++;
	if (*str == '-' || *str == '+')
	{
		*str == '-' ? sign = -1 : 1;
		str++;
	}
	while (*str)
	{
		if (*str < '0' || *str > '9')
			return (res * sign);
		res = (res * 10) + (*str - '0');
		str++;
	}
	return (res * sign);
}