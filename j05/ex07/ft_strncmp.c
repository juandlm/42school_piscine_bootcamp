int	ft_strncmp(char *str1, char *str2, unsigned n)
{
	unsigned int i;

	i = 0;
	while (i < n)
	{
		while (*str1 && *str1 == *str2)
		{
			str1++;
			str2++;
		}
		i++;
	}
	return (*str1 - *str2);
}
